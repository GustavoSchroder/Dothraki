package Server;

public class Word {
	private String english;
	private String dothraki;
	private String speechPart;
	private String pastSG;
	private String quoteEnglish;
	private String quoteDothraki;
	
	public String getEnglish() {
		return english;
	}
	
	public void setEnglish(String english) {
		this.english = english;
	}
	
	public String getDothraki() {
		return dothraki;
	}
	
	public void setDothraki(String dothraki) {
		this.dothraki = dothraki;
	}
	
	public String getSpeechPart() {
		return speechPart;
	}
	
	public void setSpeechPart(String speechPart) {
		this.speechPart = speechPart;
	}
	
	public String getPastSG() {
		return pastSG;
	}
	
	public void setPastSG(String pastSG) {
		this.pastSG = pastSG;
	}
	
	public String getQuoteEnglish() {
		return quoteEnglish;
	}
	
	public void setQuoteEnglish(String quoteEnglish) {
		this.quoteEnglish = quoteEnglish;
	}
	
	public String getQuoteDothraki() {
		return quoteDothraki;
	}
	
	public void setQuoteDothraki(String quoteDothraki) {
		this.quoteDothraki = quoteDothraki;
	}
}
